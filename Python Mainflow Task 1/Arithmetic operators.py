val1 = 2
val2 = 3

# using the addition operator
res = val1 + val2
print("Addition of value 1 and 2 is",res)



# using the subtraction operator
res = val1 - val2
print("Subtraction of value 1 and 2 is",res)


# using the multiplication operator
res = val1 * val2
print("Multplication of value 1 and 2 is",res)


# using the division operator
res = val1 / val2
print("Division of value 1 and 2 is",res)


# using the floor division
res = val1 // val2
print("Floor Division of value 1 and 2 is",res)


# using the modulus operator
res = val1 % val2
print("Modulus of value 1 and 2 is",res)


# using the exponentiation operator
res = val1 ** val2
print("Exponential of value 1 and 2 is",res)